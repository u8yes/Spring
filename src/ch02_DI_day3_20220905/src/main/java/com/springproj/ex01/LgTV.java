package com.springproj.ex01;

public class LgTV {
	
	public void turnOn() {
		System.out.println("LgTV 전원을 켜다.");
	}
	
	public void turnOff() {
		System.out.println("LgTV 전원을 끄다.");
	}
	
	public void soundUp() {
		System.out.println("LgTV 소리를 켜다.");
	}
	
	public void soundOff() {
		System.out.println("LgTV 소리를 끄다.");
	}
}
