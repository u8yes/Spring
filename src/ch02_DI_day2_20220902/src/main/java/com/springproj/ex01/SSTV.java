package com.springproj.ex01;

public class SSTV {
	public void powerOn() {
		System.out.println("SSTV 전원을 켜다.");
	}
	public void powerOff() {
		System.out.println("SSTV 전원을 끄다.");
	}
	public void volumeUp() {
		System.out.println("SSTV 볼륨을 올리다.");
	}
	public void volumeDown() {
		System.out.println("SSTV 볼륨을 내리다.");
	}
	
	
}
